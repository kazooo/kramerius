package cz.incad.kramerius.security.impl.criteria;

public class CriteriaPrecoditionException extends Exception {

    public CriteriaPrecoditionException() {
    }

    public CriteriaPrecoditionException(String message) {
        super(message);
    }

    public CriteriaPrecoditionException(String message, Throwable cause) {
        super(message, cause);
    }

    public CriteriaPrecoditionException(Throwable cause) {
        super(cause);
    }

    public CriteriaPrecoditionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
